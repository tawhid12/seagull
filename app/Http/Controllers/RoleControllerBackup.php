    public function rolePermission(Request $request, $id)
    {
        try {
            $role = Role::findOrFail(encryptor('decrypt', $id));
            $permissionId = $request->input('permissions', []);
            $syncResult = $role->permissions()->sync($permissionId);
            if (isset($syncResult['attached']) || isset($syncResult['detached']) || isset($syncResult['updated'])) 
                return redirect()->back()->with(Toastr::success('Successfully updated!', 'Success', ["positionClass" => "toast-top-right"]));
            else
                return redirect()->back()->withInput()->with(Toastr::error('Please try again!', 'Fail', ["positionClass" => "toast-top-right"]));
        } catch (Exception $e) {
            //dd($e);
            return redirect()->back()->withInput()->with(Toastr::error('Please try again!', 'Fail', ["positionClass" => "toast-top-right"]));
        }
    }
